    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("No_Transaksi");
        model.addColumn("Tanggal");
        model.addColumn("Sumber_Kas");
        model.addColumn("Keterangan");
        model.addColumn("Nominal");
        
        try{
            int No = 1 ;
            String sql ="SELECT * FROM Kas";
            java.sql.Connection conn=(Connection)konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                    model.addRow(new Object[]{No++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});          
            }
            tabelKas.setModel(model); 
        }catch (SQLException e) {
            System.out.print("Error : " + e.getMessage());
        }
    }
