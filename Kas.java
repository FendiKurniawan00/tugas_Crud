    private void tabelKasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKasMouseClicked
        // TODO add your handling code here:
        int baris = tabelKas.rowAtPoint(evt.getPoint());
        String No = tabelKas.getValueAt(baris, 1).toString();
        txtNo.setText(No);
        
        String Tanggal = tabelKas.getValueAt(baris, 2).toString();
        txtTanggal.setText(Tanggal);
        
         String Sumber = tabelKas.getValueAt(baris, 3).toString();
        cbSumber.setSelectedItem(Sumber);
        
        String Keterangan = tabelKas.getValueAt(baris, 4).toString();
        txtKeterangan.setText(Keterangan);
        
        String Nominal = tabelKas.getValueAt(baris, 5).toString();
        txtNominal.setText(Nominal);
        
    }//GEN-LAST:event_tabelKasMouseClicked

